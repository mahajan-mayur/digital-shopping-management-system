    private void signUpBtnMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_signUpBtnMouseClicked
        String rdoActionCommand = userTypeGrp.getSelection().getActionCommand();
        UserType userType = "Customer".equalsIgnoreCase(rdoActionCommand) ? UserType.CUSTOMER
                : "Admin".equalsIgnoreCase(rdoActionCommand) ? UserType.ADMIN : null;

        String firstName = this.firstNameField.getText().trim();
        String lastName = this.lastNameField.getText().trim();
        String email = this.emailField.getText().trim();
        String password = new String(this.passwordField.getPassword());
        String address = this.addressField.getText().trim();
        SignUpEvent signUpEvent = SignUpEvent
                .builder()
                .address(address)
                .email(email)
                .firstName(firstName)
                .lastName(lastName)
                .password(password)
                .userType(userType)
                .source(evt.getSource()).build();
        
        UserController userController = new UserController();
        userController.signUp(signUpEvent);

    }// GEN-LAST:event_signUpBtnMouseClicked

    private void loginBtnMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_loginBtnMouseClicked
        // TODO add your handling code here:
        publishEvent(new NavigateEvent(evt.getSource(), NavigateEvent.NavigateTo.LOGIN_PAGE));
    }// GEN-LAST:event_loginBtnMouseClicked

